# ==================== 浇口类型 → 保压时间计算 ====================
def calc_hold_time_by_gate(gate_type: str, avg_thickness: float, gate_radius: float = None,
                           gate_length: float = None, gate_width: float = None) -> float:
    """
    根据浇口类型和壁厚计算保压时间

    Args:
        gate_type: 浇口类型（直浇口/点浇口/侧浇口/护耳式浇口/其他）
        avg_thickness: 平均壁厚
        gate_radius: 圆形浇口半径
        gate_length: 矩形浇口长度
        gate_width: 矩形浇口宽度

    Returns:
        保压时间（秒）
    """
    if gate_type in ("直浇口", "护耳式浇口", "点浇口"):
        hold_time = 0.5 + 0.1 * avg_thickness
    elif gate_type == "侧浇口":
        if gate_radius is not None:
            gate_area = HSG_PI * (gate_radius ** 2)
        elif gate_length is not None and gate_width is not None:
            gate_area = gate_length * gate_width
        else:
            return 2.0  # 默认值
        hold_time = 2 * gate_area
    else:  # 其他浇口
        hold_time = 0.3 + 0.6 * (avg_thickness ** 2)

    # 约束范围
    return max(2, min(hold_time, 10))


HSG_PI = 3.141592  # 本地 π 常量

